class Usuario:
    def __init__(self, id, nombre, apellidos, username, email, telefono, ciudad, fecha_nacimiento):
        self.id = id
        self.nombre = nombre
        self.apellidos = apellidos
        self.username = username
        self.email = email
        self.telefono = telefono
        self.ciudad = ciudad
        self.fecha_nacimiento = fecha_nacimiento
